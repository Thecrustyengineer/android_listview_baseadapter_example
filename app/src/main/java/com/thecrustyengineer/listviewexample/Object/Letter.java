package com.thecrustyengineer.listviewexample.Object;

public class Letter {
    public String LETTER;
    public String WORD;

    public Letter(){
        this.LETTER = "";
        this.WORD = "";
    }

    public Letter(String LETTER, String WORD){
        this.LETTER = LETTER;
        this.WORD = WORD;
    }
}
